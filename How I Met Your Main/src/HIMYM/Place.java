package HIMYM;

public class Place {
	
	String name;
	int x;
	int y;
	
	public Place(String name, int x, int y){
		
		this.name = name;
		this.x = x;
		this.y = y;
		
	}
	
	public String getName(){
		
		return name;
		
	}
	
	public int getX(){
		
		return x;
		
	}
	
	public int getY(){
		
		return y;
		
	}

}
